					<p><span style="line-height: 1.5em;">Three leading professionals from the UX Design field generously shared their expertise with a group of two dozen attendees. Each presenter spoke for 30 minutes; a short Q&amp;A followed, and the evening concluded at Moto-i with more opportunity for discussion. Many thanks to our three terrific presenters, our deft MC Bryce Howitson, committee chair Micah Spieler and the UX Design committee, and the Smart Factory for stellar accommodations.&nbsp;</span></p>
					<p><strong>“User experience happens, whether we design for it or not.”</strong><br>
					<em>Nick Wellner, UX Designer, Periscope</em><br>
					An important take-away from Nick’s presentation was the absolute necessity of research and testing. Although lack of time/resources are a frequent challenge, a good UX designer will at the very least try to keep the user in mind throughout all phases of a project, and test frequently (even if it is with 5 strangers in a coffee shop).<br>
					Four components of the user experience process: Utility, Content, Interaction, and Visual.</p>
					<p><strong>“Innovation may be riskier, but design is valued and viewed as a competitive tool.”</strong><br>
					<em>Shaymein Ewer, UX Strategy Analyst, Best Buy</em><br>
					Shaymein started his talk by cautioning companies to make sure they are actually innovating (adding real value) instead of just imitating. By considering both business and technical requirements—and having design be an integral part of the project team from the beginning—businesses add real value and positively impact the user experience.</p>
					<p><strong>“Left brain says: If, then. Right brain asks: What if?”</strong><br>
					<em>Matt Johnson, Partner: Creative + Strategy Director, GoKart</em><br>
					Matt spent some time describing the role that emotion plays in effective user experience. You can either spend money to plan the user experience before launch, or you WILL spend money to fix the user experience after launch! To create great user interactions:</p>
					<ul>
					<li>Get to know your user: Study the data, meet your users, understand the constraints.</li>
					<li>Collaborate: UX design is a methodology, a mindset, and a team sport.</li>
					<li>Sprint: Prototype and test with end users.</li>
					</ul>
					<p><em>Post written by Kris Thayer, AIGA Minnesota Member and UX Designers committee volunteer.</em></p>
									
